$(document).ready(function(){
	
	$(".artist").click(function(){
		$("#directions").fadeOut();
		var id = $(this).attr("id");
		var parts = id.split('-');
		// Will result in partsArray[0] == '123', partsArray[1] == 'abc', etc
		var artistID = parts[0];
		var albumID = parts[1];
		
		$(".artist").css("opacity", ".2");
		$(this).css("opacity", "1");
		
		songsToAlbum(artistID);
		songToControl(artistID);
	});
	
	$(".name").click(function(){
		var who = $(this).text();
		searchArtist(who);
		
	});
	
	function songToControl(artistID){
		//is there a more concise way to do this?
		$(".name").fadeOut();
		$(".name").empty();
		$(".name").hide().append(musicArray[artistID].artist);
		$(".name").fadeIn("fast");
		
		$(".info").fadeOut();
		$(".info").empty();
		$(".info").hide().append(musicArray[artistID].albums[0].title);
		$(".info").fadeIn("fast");
		
		$(".genres").fadeOut();
		$(".genres").empty();
		$(".genres").hide().append(musicArray[artistID].albums[0].genre);
		$(".genres").fadeIn("fast");
		
		$("#music").empty().append("<source src=\"tunez/"+musicArray[artistID].sample+"\" type=\"audio/mp3\" />");
		
	};
	
	function songsToAlbum(artistID){
		$("#"+artistID+" img").css("opacity",".3");
		
		for(var j=0; j<musicArray[artistID].albums.length; j++){
			$("#"+artistID).append("<ul>");
			//console.log(musicArray[artistID].albums);
			for(var k=0; k<musicArray[artistID].albums[j].songs.length;k++){
				//$("#"+artistID).append("<li>"+musicArray[artistID].albums[j].songs[k]+"</li>");
				
			}
			$("#"+artistID).append("</ul>");
		}
		
	}
	
	function searchArtist(who){		
		for(var i=0; i<musicArray.length; i++){
			for(var j=0; j<musicArray[i].albums.length; j++){
				if(musicArray[i].artist == who){
					$("#"+i+"-"+j).css({
						"opacity": "1"
					});
				}else{
				
				}
			}

		}
	}
});


